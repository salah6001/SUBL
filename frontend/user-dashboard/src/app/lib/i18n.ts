// Lightweight i18n for the user dashboard. Two locales: English ("en-US",
// the default) and Arabic ("ar", right-to-left). The active locale comes from
// the user's saved `language` preference; anything that isn't an Arabic tag
// falls back to English.

export type Lang = "en" | "ar";

/** Normalise a stored BCP-47-ish tag ("en-US", "ar", "ar-EG") to our Lang. */
export function toLang(language: string | undefined | null): Lang {
  return (language ?? "").toLowerCase().startsWith("ar") ? "ar" : "en";
}

/** Text direction for a locale. */
export function dirFor(lang: Lang): "rtl" | "ltr" {
  return lang === "ar" ? "rtl" : "ltr";
}

